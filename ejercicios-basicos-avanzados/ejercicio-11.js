const mixedElements = [
    6,
    1,
    "Marvel",
    1,
    "hamburguesa",
    "10",
    "Prometeo",
    8,
    "Hola mundo",
];

function averageWord(list) {
    let numero = 0;
    for (const element of list) {
        if(typeof element === "string") {
            numero += element.length;
        } else {
            numero += element;
        };
    };
    return numero;
};

console.log(averageWord(mixedElements));